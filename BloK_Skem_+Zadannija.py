l = float(input("Введите длину ткани (в метрах): "))

while l > 0:
    запрос = float(input("Введите длину куска ткани: "))
    if запрос > l:
        print(f"Ткани не хватает. Осталось {l:.2f} м.")
        согласие = input("Купить остаток ткани? (да/нет): ").strip().lower()
        if согласие == "да":
            print(f"Остаток {l:.2f} м продан. Программа завершена.")
            break
        else:
            print("Продажа отменена. Переходим к следующему клиенту.")
    else:
        l -= запрос
        print(f"Осталось ткани: {l:.2f} м.")

if l == 0:
    print("Ткань закончилась. Программа завершена.")


