while True:
    print("\nВычисление площади трапеции.")
    a = float(input("Введите длину первого основания (a): "))
    b = float(input("Введите длину второго основания (b): "))
    h = float(input("Введите высоту (h): "))

    S = ((a + b) * h) / 2
    print(f"Площадь трапеции: {S:.2f}")

    продолжить = input("Хотите вычислить ещё одну трапецию? (да/нет): ").strip().lower()
    if продолжить != "да":
        print("Программа завершена. До свидания!")
        break

