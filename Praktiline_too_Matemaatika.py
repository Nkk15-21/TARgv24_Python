import random

print("Добро пожаловать в Математическую Викторину!")
print("Выберите уровень сложности:")
print("1 - Легкий (только сложение)")
print("2 - Средний (сложение и вычитание)")
print("3 - Сложный (сложение, вычитание, умножение, деление)")

уровень = None
while уровень not in [1, 2, 3]:
    try:
        уровень = int(input("Введите уровень (1, 2 или 3): "))
    except ValueError:
        print("Неверный ввод. Пожалуйста, выберите 1, 2 или 3.")

диапазон_чисел = None
while диапазон_чисел is None:
    try:
        диапазон_чисел = int(input("Введите максимальный диапазон чисел (например, 10, 50, 100): "))
    except ValueError:
        print("Неверный ввод. Попробуйте еще раз.")

количество_вопросов = None
while количество_вопросов is None:
    try:
        количество_вопросов = int(input("Введите количество вопросов: "))
    except ValueError:
        print("Неверный ввод. Попробуйте еще раз.")

очки = 0
for i in range(количество_вопросов):
    print(f"\nВопрос {i + 1} из {количество_вопросов}")
    операции = {1: ['+'], 2: ['+', '-'], 3: ['+', '-', '*', '/']}
    операция = random.choice(операции[уровень])

    число1 = random.randint(1, диапазон_чисел)
    число2 = random.randint(1, диапазон_чисел)

    if операция == '/':
        while число2 == 0:
            число2 = random.randint(1, диапазон_чисел)

    пример = f"{число1} {операция} {число2}"
    правильный_ответ = round(eval(пример), 2)
    print("Решите:", пример)

    ответ_пользователя = None
    while ответ_пользователя is None:
        try:
            ответ_пользователя = float(input("Ваш ответ: "))
        except ValueError:
            print("Неверный ввод. Попробуйте еще раз.")

    if ответ_пользователя == правильный_ответ:
        print("Верно!")
        очки += 1
    else:
        print(f"Неверно. Правильный ответ: {правильный_ответ}")

print("\nВикторина завершена!")
процент = (очки / количество_вопросов) * 100
print(f"Ваш результат: {процент:.2f}%")

if процент < 60:
    оценка = 2
elif 60 <= процент < 75:
    оценка = 3
elif 75 <= процент < 90:
    оценка = 4
else:
    оценка = 5

print(f"Ваша оценка: Hinne {оценка}")
